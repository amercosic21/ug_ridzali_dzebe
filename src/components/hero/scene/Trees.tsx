type Layer = [points: string, fill: string];

interface Tree {
  x: number;
  y: number;
  /** Trunk width; the trunk is centered (x = -w/2, y = -(w/2 + 1)). */
  w: number;
  h: number;
  trunk: string;
  /** Three stacked foliage triangles, back to front. */
  layers: Layer[];
}

/** A layered pine tree: a trunk rect plus three triangle tiers. */
function PineTree({ x, y, w, h, trunk, layers }: Tree) {
  return (
    <g transform={`translate(${x}, ${y})`}>
      <rect x={-w / 2} y={-(w / 2 + 1)} width={w} height={h} fill={trunk} />
      {layers.map(([points, fill], i) => (
        <polygon key={i} points={points} fill={fill} />
      ))}
    </g>
  );
}

function TreeCluster({ trees }: { trees: Tree[] }) {
  return (
    <g>
      {trees.map((tree, i) => (
        <PineTree key={i} {...tree} />
      ))}
    </g>
  );
}

